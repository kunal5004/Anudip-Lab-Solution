#Compute the median of the flattened NumPy array
#import Numpy Library
import numpy as np
#Input array
x_odd = np.array([1, 2, 3, 4, 5, 6, 7])
#Use median Fucnction and store in variable result.
result=np.median(x_odd)
#Display the Array & Median
print("Array:",x_odd)
print("The Median is:",result)

#Output
'''
Array: [1 2 3 4 5 6 7]
The Median is: 4.0
'''
