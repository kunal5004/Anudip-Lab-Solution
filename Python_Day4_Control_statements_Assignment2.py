# Create a Python program that checks whether a person is eligible to vote (18 years or older) based on their age.

# Set the age variable 
age = 20

# Check if the age is greater than or equal to 18
if age >= 18:
    # If the condition is true (age is 18 or older), print that the person is eligible to vote
    print("You are Eligible to Vote")
else:
    # If the condition is false (age is less than 18), print that the person is not eligible to vote
    print("You are not Eligible to Vote")

#output
#You are Eligible to Vote
