#Using input function take two number and then swap the number

# Taking two numbers as input from the user
num1 = input("Enter the first number: ")
num2 = input("Enter the second number: ")

# Swapping the numbers
num1, num2 = num2, num1

# Printing the swapped numbers
print(f"After swapping: First number is {num1}, Second number is {num2}")

#output
#Enter the first number: 5
#Enter the second number: 3
#After swapping: First number is 3, Second number is 5
