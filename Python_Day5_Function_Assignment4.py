# Accept a name from the user and display that in lower case using lower() function.

# Get input from the user and store it in the variable 'Name'.
Name=input("Enter your name: ")
# Convert the name to lowercase using the lower() function.
# Use lower() to convert the input string to lowercase.
lowercase= Name.lower()
# Display the name in lowercase.
print(f"Your name in lowercase is: {lowercase}")

#output
'''
Enter your name: KUNAL
Your name in lowercase is: kunal
'''
