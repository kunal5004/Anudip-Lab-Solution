#Write a Python program to find the number of times 4 appears in the tuple.

#Create a tuple.
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 )
#Use count to find how many time 4 appeared in tuple.
result= tuplex.count(4)
#print the result.
print(f"Number of times 4 appears: {result}")

#Output
#Number of times 4 appears: 3
